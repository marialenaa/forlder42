int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int i;

	i = 1;
	while (s1 && s2 && i <= n)
	{
		if (s1[i] == s2[i])
			i++;
	}
	return (s1[i] - s2[i]);
}
