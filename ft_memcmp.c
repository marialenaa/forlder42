#include <libft.h>

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	while (s1 && s2 && n)
	{
		if(*s1 == *s2)
		{
			s1++;
			s2++;
			n--;
		}
		return ((int)*s1 - (int)*s2);
	}
}
